let daynumber = prompt("enter the day number e.g. 1,2 ,3...");
switch (daynumber) {
    case "1":
        alert("this is monday");
        break;
    case "2":
        alert("this is tuesday");
        break;
    case "3":
        alert("this is wednesday");
        break;
    case "4":
        alert("this is thursday");
        break;
    case "5":
        alert("this is friday");
        break;
    case "6":
        alert("this is saturday");
        break;
    case "7":
        alert("this is sunday");
        break;
    default:
        alert("invalid day number");
        break;
}